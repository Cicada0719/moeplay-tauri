import assert from "node:assert/strict";
import { execFile } from "node:child_process";
import path from "node:path";
import test from "node:test";
import { promisify } from "node:util";
import { fileURLToPath } from "node:url";

import {
  inspectCommandContract,
  kebabToSnake,
  parseFrontendInvokeCommands,
  snakeToKebab,
} from "./verify-command-contract.mjs";

const execFileAsync = promisify(execFile);
const scriptsDirectory = path.dirname(fileURLToPath(import.meta.url));
const fixturesDirectory = path.join(scriptsDirectory, "fixtures", "command-contract");
const verifierPath = path.join(scriptsDirectory, "verify-command-contract.mjs");

test("converts capability kebab-case and Rust snake_case command names", () => {
  assert.equal(kebabToSnake("preview-directory-for-games"), "preview_directory_for_games");
  assert.equal(kebabToSnake("pick-image-file"), "pick_image_file");
  assert.equal(snakeToKebab("import_selected_candidates"), "import-selected-candidates");
});

test("extracts literal invokeCmd calls with TypeScript generic arguments", () => {
  const parsed = parseFrontendInvokeCommands(`
    // invokeCmd("ignored_comment")
    const text = 'invokeCmd("ignored_string")';
    invokeCmd<Result<Map<string, number>>>("foo_bar", { value: 1 });
    invokeCmd(\`pick_image_file\`);
  `, "fixture.ts");

  assert.deepEqual(parsed.commands, ["foo_bar", "pick_image_file"]);
  assert.deepEqual(parsed.dynamicCalls, []);
});

test("accepts a consistent command contract and frontend subset", async () => {
  const report = await inspectCommandContract(path.join(fixturesDirectory, "valid"));
  assert.equal(report.ok, true);
  assert.deepEqual(report.counts, {
    generateHandler: 2,
    buildCommands: 2,
    capabilityCommands: 2,
    generatedPermissionCommands: 2,
    frontendCommands: 1,
  });
});

test("prints every difference and exits non-zero for an inconsistent contract", async () => {
  await assert.rejects(
    execFileAsync(process.execPath, [verifierPath, "--root", path.join(fixturesDirectory, "invalid")]),
    (error) => {
      assert.equal(error.code, 1);
      assert.match(error.stdout, /Missing from build\.rs COMMANDS/);
      assert.match(error.stdout, /baz_qux/);
      assert.match(error.stdout, /Extra in build\.rs COMMANDS/);
      assert.match(error.stdout, /extra_build/);
      assert.match(error.stdout, /Missing custom capability allow permissions/);
      assert.match(error.stdout, /Extra custom capability allow permissions/);
      assert.match(error.stdout, /Missing autogenerated command permission files/);
      assert.match(error.stdout, /Extra autogenerated command permission files/);
      assert.match(error.stdout, /Frontend invokeCmd commands not registered/);
      assert.match(error.stdout, /unknown_frontend/);
      return true;
    }
  );
});
