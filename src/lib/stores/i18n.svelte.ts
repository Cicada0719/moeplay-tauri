let _lang = $state(localStorage.getItem("moegame-lang") || "zh");
const strings: Record<string, Record<string, string>> = { zh, en };

export const i18n = {
  get lang() { return _lang; },
  set lang(v: string) { _lang = v; localStorage.setItem("moegame-lang", v); },
  get languages() { return Object.keys(strings); },

  t(key: string, params?: Record<string, string | number>): string {
    const dict = strings[_lang] ?? strings.zh;
    let text = dict[key] ?? strings.zh[key] ?? key;
    if (params) {
      for (const [k, v] of Object.entries(params)) {
        text = text.replace(`{${k}}`, String(v));
      }
    }
    return text;
  },
};
