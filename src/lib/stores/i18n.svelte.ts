let _lang = $state(localStorage.getItem("moegame-lang") || "zh");
const strings: Record<string, Record<string, string>> = { zh, en };

export const i18n = {
  get lang() { return _lang; },
  set lang(v: string) { _lang = v; localStorage.setItem("moegame-lang", v); },
  get languages() { return Object.keys(strings); },
  // BCP47 locale，供日期/时间格式化随界面语言联动。
  get locale() { return _lang === "zh" ? "zh-CN" : "en-US"; },

  t(key: string, params?: Record<string, string | number>): string {
    const dict = strings[_lang] ?? strings.zh;
    let text = dict[key] ?? strings.zh[key] ?? key;
    if (params) {
      for (const [k, v] of Object.entries(params)) {
        text = text.replace(`{${k}}`, String(v));
      }
    }
    return text;
  },
};

// 启动对齐：仅当 localStorage 没有用户显式选择（moegame-lang 为空）时，
// 首次 settingsStore.load() 完成后用后端 settings.language 兜底一次。
// 对齐走 setter 写入 localStorage，此后语言以用户切换为唯一来源，不再被后端覆盖。
$effect.root(() => {
  $effect(() => {
    if (!settingsStore.loaded) return;
    if (localStorage.getItem("moegame-lang") !== null) return;
    const backendLang = settingsStore.language;
    if (backendLang && strings[backendLang]) i18n.lang = backendLang;
  });
});
